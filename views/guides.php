        <h2 class="text-center mt-3 display-3 fw-bold">Lexique</h2>
        <h3 class="mt-3 display-5 fw-bold">Les différents coups du jeu :</h3>
        <p>
            Les coups du jeu sont plus connus par la communauté par leur terme anglais. C'est pourquoi un lexique 
            peut s'avérer utile pour un nouveau joueur.
        </p>
        <h4>Coups au sol</h4>
        <p>Les coups au sol ne sont faisables qu'en étant au sol, sur le stage ou sur une plateforme.</p>
        <h5>Jab</h5>
        <p>
            Le "jab" est le "coup simple". Il suffit d'appuyer sur la touche A pour le faire. Certains personnages 
            possèdent deux versions du jab. Une version que l'on appellera "jab gentleman", qui est faisable en appuyant trois 
            fois sur le bouton A pour frapper rapidement l'adversaire, et une version "rapid jab", qui est faisable en 
            appuyant trois fois sur le bouton A pour frapper plusieurs fois l'adversaire. Certains persos n'ont qu'une 
            version du jab et d'autres ont les deux.
        </p>
        <h5>Tilts</h5>
        <p>
            Les "tilts" sont des coups que l'on réalise en appuyant sur A et en mettant le stick directionnel dans une 
            direction. Il en existe trois :
        </p>
        <div class="col-10 offset-1">
            <ul>
                <li>
                    <h6>Le f-tilt</h6>
                    <p>
                        Le "f-tilt" ou "forward tilt" est le coup réalisé lorsque l'on appuie sur A et en mettant le stick 
                        directionnel à droite ou à gauche. On peut donner à certains f-tilts un angle pour toucher plus bas 
                        ou plus haut en mettant un léger angle en haut ou en bas avec le stick directionnel.
                    </p>
                </li>
                <li>
                    <h6>Le up tilt</h6>
                    <p>
                        Le "up tilt" ou "upward tilt" est le coup réalisé lorsque l'on appuie sur A et en mettant le stick 
                        directionnel vers le haut.
                    </p>
                </li>
                <li>
                    <h6>Le down tilt</h6>
                    <p>
                        Le "down tilt" ou "downward tilt" est le coup réalisé lorsque l'on appuie sur A et en mettant le stick 
                        directionnel vers le bas.
                    </p>
                </li>
            </ul>
        </div>
        <h5>Attaques smashs</h5>
        <p>
            Les attaques smash sont les attaques les plus puissantes des personnages. Elles sont surtout utilisées pour 
            éjecter l'adversaire et peuvent être chargées pendant quelques secondes. Elles sont réalisables en appuyant 
            sur A et en mettant le stick directionnel dans une direction en même temps. Comme les tilts, il 
            en existe trois :
        </p>
        <div class="col-10 offset-1">
            <ul>
                <li>
                    <h6>Le f-smash</h6>
                    <p>
                        Le "f-smash" ou "forward smash" est le coup réalisé quand on appuie sur A et en mettant le stick 
                        directionnel en même temps à droite ou à gauche. Certains f-smash peuvent également être anglés, 
                        comme pour les tilts, pendant le chargement de l'attaque il suffit de légèrement angler le 
                        stick directionnel vers le haut ou le bas pour toucher plus haut ou plus bas.
                    </p>
                </li>
                <li>
                    <h6>Le up smash</h6>
                    <p>
                        Le "up smash" ou "upward smash" est le coup réalisé quand on appuie sur A et en mettant le stick 
                        directionnel en même temps vers le haut.
                    </p>
                </li>
                <li>
                    <h6>Le down smash</h6>
                    <p>
                        Le "down smash" ou "downward smash" est le coup réalisé quand on appuie sur A et en mettant le stick 
                        directionnel en même temps vers le bas.
                    </p>
                </li>
            </ul>
        </div>
        <h4>Coups aériens</h4>
        <p>
            Les coups aériens sont les coups réalisables lorsque le personnage se trouve dans les airs, après un saut 
            ou une éjection. Ils sont réalisables en appuyant sur A et en mettant le stick dans une direction. Il en 
            existe 5 :
        </p>
        <div class="col-10 offset-1">
            <ul>
                <li>
                    <h5>Le nair</h5>
                    <p>
                        Le "nair" ou "neutral air" est un coup performé lorsqu'on appuie seulement sur A lorsque notre 
                        personnage est en l'air.
                    </p>
                </li>
                <li>
                    <h5>Le fair</h5>
                    <p>
                        Le "fair" ou "forward air" est un coup performé lorsqu'on appuie sur A et en mettant le stick dans 
                        la direction où regarde notre personnage quand il est en l'air.
                    </p>
                </li>
                <li>
                    <h5>Le bair</h5>
                    <p>
                        Le "bair" ou "back air" est un coup performé lorsqu'on appuie sur A et en mettant le stick dans 
                        la direction du dos de notre personnage quand il est en l'air.
                    </p>
                </li>
                <li>
                    <h5>Le u-air</h5>
                    <p>
                        Le "u-air" ou "up air" est un coup performé lorsqu'on appuie sur A et en mettant le stick vers le 
                        haut quand notre personnage est en l'air.
                    </p>
                </li>
                <li>
                    <h5>Le dair</h5>
                    <p>
                        Le "dair" ou "down air" est un coup performé lorsqu'on appuie sur A et en mettant le stick vers le 
                        bas quand notre personnage est en l'air.
                    </p>
                </li>
            </ul>
        </div>
        <h4>Coups spéciaux</h4>
        <p>
            Les coups spéciaux sont des coups activables en appuyant sur le bouton B. Ils sont faisables au sol et 
            en l'air. Il en existe 4 (sauf pour Terry qui en a 5) :
        </p>
        <div class="col-10 offset-1">
            <ul>
                <li>
                    <h5>Le neutral B</h5>
                    <p>
                        Le "neutral B" est le coup que l'on réalise quand on appuie sur le bouton B.
                    </p>
                </li>
                <li>
                    <h5>Le side B</h5>
                    <p>
                        Le "side B" est le coup que l'on réalise quand on appuie sur le bouton B avec le stick directionnel 
                        incliné à droite ou à gauche.
                    </p>
                </li>
                <li>
                    <h5>Le down B</h5>
                    <p>
                        Le "down B" est le coup que l'on réalise quand on appuie sur le bouton B avec le stick directionnel 
                        incliné vers le bas.
                    </p>
                </li>
                <li>
                    <h5>Le up B</h5>
                    <p>
                        Le "up B" est le coup que l'on réalise quand on appuie sur le bouton B avec le stick directionnel 
                        incliné vers le haut. Cette attaque est particulière car elle n'a pas pour but premier de servir de 
                        "kill move", donc à ne pas tuer l'adversaire, mais plutôt d'aider le personnage à remonter sur le 
                        stage lorque le saut ne suffit pas.
                    </p>
                </li>
                <li>
                    <h5>Concernant Terry</h5>
                    <p>
                        Terry sort du lot par rapport au reste des personnages. En effet il possède deux coups différents 
                        pour le side B qui sont nommé Forward B, appui sur le bouton B et stick directionnel dans la direction 
                        où regarde Terry, et Back B, appui sur le bouton B et stick directionnel vers le dos de Terry.
                    </p>
                </li>
            </ul>
        </div>
        <h4>Saisie</h4>
        <p>
            La saisie ou le "grab" sert principalement d'outils pour contrer un adversaire qui ne fait que se protéger 
            avec son bouclier. Le grab permet de lancer des combos, on l'appelle "combo starter" pour certains personnages. 
            Après avoir saisit l'adversaire, vous pouvez le lancer dans 4 directions qu'on appelle des "throws" :
        </p>
        <div class="col-10 offset-1">
            <ul>
                <li>
                    <h5>La f-throw</h5>
                    <p>
                        Pour effectuer une f-throw ou "forward throw" il faut saisir l'adversaire puis mettre le stick 
                        directionnel dans la direction où regarde votre personnage.
                    </p>
                </li>
                <li>
                    <h5>La b-throw</h5>
                    <p>
                        Pour effectuer une b-throw ou "back throw" il faut saisir l'adversaire puis mettre le stick 
                        directionnel derrière votre personnage.
                    </p>
                </li>
                <li>
                    <h5>La u-throw</h5>
                    <p>
                        Pour effectuer une u-throw ou "up throw" il faut saisir l'adversaire puis mettre le stick 
                        directionnel vers le haut.
                    </p>
                </li>
                <li>
                    <h5>La d-throw</h5>
                    <p>
                        Pour effectuer une d-throw ou "down throw" il faut saisir l'adversaire puis mettre le stick 
                        directionnel vers le bas.
                    </p>
                </li>
            </ul>
        </div>
        <h4>Bouclier</h4>
        <p>
            Le bouclier s'active quand on appuie sur la gâchette droite de la manette. Il permet de se protéger des attaques. 
            Il a une durée limitée et lorsqu'il se brise votre personnage est étourdi et ne peut plus agir pendant plusieurs 
            secondes. En plus du bouclier cette touche de faire des esquives au sol, en l'air et des roulades pour éviter 
            les attaques adverses et prendre l'avantage :
        </p>
        <div class="col-10 offset-1">
            <ul>
                <li>
                    <h5>La roll in</h5>
                    <p>
                        La "roll in" désigne une roulade vers l'adversaire. Vous pouvez la faire avec la gâchette de 
                        bouclier et le stick directionnel vers votre adversaire.
                    </p>
                </li>
                <li>
                    <h5>La roll out</h5>
                    <p>
                        La "roll out" désigne une roulade dans la direction opposée à votre adversaire. Vous pouvez 
                        la faire avec la gâchette de bouclier et le stick directionnel vers votre adversaire.
                    </p>
                </li>
                <li>
                    <h5>La spot dodge</h5>
                    <p>
                        La "spot dodge" désigne une esquive sur place. Vous pouvez la faire avec la gâchette de 
                        bouclier et le stick directionnel vers le bas.
                    </p>
                </li>
                <li>
                    <h5>L'airdodge</h5>
                    <p>
                        "L'airdodge" désigne une esquive aérienne. Vous pouvez la faire avec la gâchette de 
                        bouclier lorsque vous êtes en l'air.
                    </p>
                </li>
            </ul>
        </div>
