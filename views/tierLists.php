        <h2 class="text-center mt-3 display-3">Présentation / Définition</h2>
        <p>
            Une tier List est une liste ordonnée des personnages du jeu pour les classer en fonction de leur force 
            du meilleur au pire dans un contexte de haut niveau compétitif. Le classement va, en général, de S, pour désigner 
            les meilleurs personnages, souvent appelés "top tier", jusqu'à D ou E, souvent appelés "bottom tier". 
            Ces listes sont à ne prendre pas trop prendre au sérieux pour les nouveaux joueurs car elles 
            ne veulent pas dire la même chose à ce niveau. Ces listes sont réalisées par des joueurs et les plus viables 
            sont généralement celles des joueurs professionnels, même si certaines restent discutables.
        </p>
